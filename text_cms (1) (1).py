import re

def url_risk_score(message):
    urls = re.findall(r'(https?://\S+)', message)

    if not urls:
        return 0   # no URL → no URL risk

    score = 0
    url = urls[0]

    # Rule 1: http instead of https
    if url.startswith("http://"):
        score += 30

    # Rule 2: suspicious words
    suspicious_words = ["verify", "login", "bank", "update", "secure"]
    for word in suspicious_words:
        if word in url.lower():
            score += 15

    # Rule 3: long URL
    if len(url) > 50:
        score += 20

    return min(score, 100)

