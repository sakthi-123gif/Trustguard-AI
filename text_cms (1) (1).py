def final_risk_score(message):
    text_score = text_risk_score(message)
    url_score = url_risk_score(message)

    final_score = (0.5 * text_score) + (0.5 * url_score)

    # Security override rule
    if url_score >= 70:
        final_score = 85   # force high-risk scenario

    return round(final_score, 2)

